package com.springapp.mvc.model.eventChangetype;

import javax.persistence.*;

/**
 * Created by tabbasi on 12/10/14.
 */
@Entity
@Table(name = "EventChangeType", schema = "", catalog = "mydb")
public class EventChangeTypeEntity {
    private int id;
    private String type;

    @Id
    @Column(name = "Id")
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Basic
    @Column(name = "Type")
    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        EventChangeTypeEntity that = (EventChangeTypeEntity) o;

        if (id != that.id) return false;
        if (type != null ? !type.equals(that.type) : that.type != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id;
        result = 31 * result + (type != null ? type.hashCode() : 0);
        return result;
    }
}
